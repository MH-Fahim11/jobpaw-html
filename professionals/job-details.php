                <div class="sec-title text-center">
                    <h6 class="sec-title__tagline">Fonctions</h6>
                </div>
                <p>Résumé du poste : <br>
                    Les facilitateurs du système de marché résilient (RMSF) (assistant de programme, système de 
                    marché résilient) seront les facilitateurs de première ligne des activités du PARE et seront 
                    chargés de soutenir les partenariats et la collaboration avec les petites, moyennes et grandes 
                    entreprises et organisations dans le système de marché du bétail. Le RMSF encouragera les 
                    initiatives de développement de la production animale et de l'agro-industrie dans des communes 
                    sélectionnées dans trois départements : Nord, Nord-Est et Centre. Le RMSF établira des 
                    relations et se coordonnera étroitement avec les principales parties prenantes du secteur privé, 
                    des éleveurs et du secteur public, les institutions financières (par exemple, les banques, les 
                    institutions de microfinance et d'autres institutions de prêt) et d'autres acteurs du 
                    développement afin de faciliter les activités axées sur le marché. Le RMSF sera en liaison 
                    étroite avec le bureau d'activité du PARE à Cap Haïtien afin de fournir en permanence un
                    retour d'information et d'apprentissage pour le projet. Les facilitateurs de systèmes de marché 
                    résilients (RMSF) rendront compte au directeur technique de l'élevage. 
                    Principales responsabilités :<br>
                    
                    • Apporter une contribution à l'examen des PME partenaires du pipeline existant pour des 
                    investissements potentiels qui feront l'objet d'un appel à candidatures ;<br>

                    • Aider à l'examen des candidats des communes assignées qui demandent des subventions 
                    de co-investissement dans le cadre du système de marché du bétail ;<br>

                    • Faciliter et aider le personnel technique du PARE à co-créer des applications avec des 
                    partenaires à fort potentiel et à concevoir des paquets d'assistance technique qui 
                    répondent aux critères programmatiques ;<br>

                    • Collaborer avec le spécialiste du respect de l'environnement pour suivre les progrès et la
                    conformité des bénéficiaires et effectuer les contrôles environnementaux ;<br>

                    • Collaborer avec le/la spécialiste genre et de jeunesse pour s'assurer que les demandes et 
                    les co-investissements financés créent des opportunités et renforcent l'équité pour les 
                    femmes et les jeunes ;<br>

                    • Contrôler les subventions de co-investissement et les interventions au niveau communal 
                    pour soutenir les activités de production et de commercialisation du bétail, et veiller à ce 
                    que les objectifs et les étapes de la subvention soient suivis et atteints ; <br>

                    • Veiller à ce que les bénéficiaires des co-investissements respectent le plan de surveillance 
                    et d'atténuation des effets sur l'environnement (EMMP) et tenir à jour le dossier de 
                    surveillance en collaboration avec le spécialiste de l'environnement ;<br>

                    • Aider au développement de relations avec les parties prenantes clés du secteur privé et 
                    des entités du gouvernement haïtien, des institutions financières et d'autres activités de 
                    développement qui sont opérationnelles dans les communes sélectionnées ; <br>

                    • Fournir un soutien direct aux bénéficiaires qui fournissent des formations ou des liens 
                    avec les entreprises aux participants ; <br>

                    • Collaborer avec les principales institutions financières locales pour mieux faire connaître 
                    les possibilités et les contraintes de financement des MPME dans le secteur de 
                    l'agroalimentaire, développer des modèles de financement innovants pour soutenir le 
                    développement de l'agro-industrie ;<br>

                    • Coordonner étroitement avec le responsable du suivi et de l'évaluation et la liaison avec 
                    les systèmes de marché pour recueillir des données solides et en temps réel afin 
                    d'informer l'apprentissage et l'adaptation du projet ; <br>

                    • Solliciter un retour d'information qualitatif de la part des organisations partenaires tous 
                    les trimestres dans le cadre des sessions "Pause et réflexion".
                </p>
