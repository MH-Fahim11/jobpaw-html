                    <h5>L’orientation professionnelle</h5>
                    <p>L’orientation professionnelle est une combinaison de quatre éléments clés :</p>

                    <h5>a. Les préférences.</h5>
                    <p>Il est clair que chacun, influencé par l’hérédité et par l’environnement, ait des préférences
                        naturelles sur les professions qu’il voudrait choisir.</p>
                    <p>Ce choix est, un peu, conditionné à la naissance, de par nos parents de qui nous héritons une
                        partie de nos gênes (Loi de Mendel). Plus tard, il sera influencé par nos amis, nos
                        fréquentations, nos observations, notre entourage, etc.</p>
                    <p>Parfois, des divergences frontales apparaissent entre ces types d’influence et l’individu se
                        trouve face à un dilemme sur quoi choisir.</p>

                    <h5>b. Les aptitudes.</h5>
                    <p>Les professions nécessitent toutes un minimum de prérequis, de savoir accumulé. Il est donc
                        naturel que, en plus de la volonté, la capacité d’apprentissage ou l’aptitude joue un rôle
                        central dans la profession à choisir.</p>
                    <p>Ceci est clé pour surmonter les potentiels obstacles pour appréhender et réussir les cours.
                        Evidemment, de rares individus, mus par une hargne et une volonté féroce, défient cette loi
                        naturelle et arrivent à se surpasser et à exceller dans des domaines où ils étaient très peu
                        aptes.</p>

                    <h5>c. La rentabilité économique.</h5>
                    <p>En dépit de nos préférences et de nos aptitudes, nous ne choisissons pas une profession si elle
                        ne nous rapporte aucune satisfaction économique, au-delà de la satisfaction personnelle. Ceci
                        obéit au postulat naturel : sans motivation, nul n’agit.</p>
                    <p>La rentabilité économique (définie ici comme la valeur de l’effort, du travail) est fondamentale
                        pour nous garder motivés à travers le temps. Il existe des statistiques relatives aux salaires
                        de différentes professions et aux professions les plus demandées sur le marché. Elles sont
                        disponibles ici.</p>

                    <h5>d. L’impact social.</h5>
                    <p>Tout être humain aime être apprécié, valorisé pour ce qu’il fait. Ceci est d’autant plus facile
                        si l’individu exerce une profession qui a un impact social et qui améliore le bien-être des
                        autres.</p>
                    <p>Beaucoup de pays font face à des défis énormes liés au chômage, la faim, l’ignorance,
                        l’instabilité, l’inégalité, la pauvreté, la gouvernance, etc. De plus en plus de jeunes en
                        prennent conscience et veulent apporter leur contribution en apprenant un métier qui leur
                        permettra d’apporter des éléments de solution.</p>
                    <p>En cas de doute, il y a aussi, en ligne, des tests d’orientation professionnelle sur Kledou - la
                        clé de votre orientation.</p>
                    <p>Si vous avez besoin d’une aide personnalisée, organisez-vous avec votre école et vous vous
                        offrirons une formation gratuite. Vous pouvez <a href="#">cliquer ici et nous contacter</a>.</p>

                    <h5>Insertion professionnelle</h5>
                    <p>L’insertion professionnelle est basée sur trois éléments fondamentaux :</p>

                    <h5>a. La confiance en soi et la connaissance de soi</h5>
                    <p>La confiance en soi et la connaissance de soi permettent de faire une auto-évaluation objective
                        en identifiant ses forces et ses faiblesses.</p>
                    <p>Il est important de connaitre ses points forts et de les mettre en valeur. Ceux-ci sont
                        différents à chaque individu et sont même spécifiques à chaque individu. Il est aussi
                        fondamental de détecter ses faiblesses afin de les diminuer.</p>
                    <p>L’identification de nos forces et faiblesses permet de booster notre confiance entre nous-mêmes,
                        laquelle est un vrai sérum dans le choix et la poursuite de nos objectifs.</p>

                    <h5>b. Le contenu des outils (CV, lettre de motivation) de recherche d’emploi</h5>
                    <p>Toute offre/demande d’emploi (sur une base compétitive ou recommandée) s’accompagne
                        nécessairement d’un CV. Ce dernier, entre notre absence, joue le rôle de notre porte-parole, de
                        notre messager.</p>
                    <p>Sa précision et sa véracité sont déterminantes pour attirer l’attention des recruteurs.
                        Concrètement, le CV doit, à chaque fois, faire voir le lien entre ce que vous avez déjà fait
                        (expériences/formation) et ce que vous allez faire (taches/formation). Vous serez toujours
                        appelé en entrevue si vous arrivez toujours à démontrer ce lien.</p>
                    <p>Les spécialistes en ressources humaines suggèrent que le CV soit sur une page ou deux pages
                        maximum et contienne : informations personnelles (prénom et nom, tel et e-mail) et informations
                        professionnelles (titre, compétences clés, expériences/réalisations, éducation, etc.).</p>
                    <p>Il existe des modèles de CV et l’outil de CVPaw Pro pour vous aider à confectionner votre CV.</p>
                    <p>Si vous avez besoin d’une aide personnalisée, organisez-vous avec votre école et vous vous
                        offrirons une formation gratuite. Vous pouvez <a href="#">cliquer ici et nous contacter</a>.</p>

                    <h5>c. La préparation et la réussite de l’entrevue</h5>
                    <p>Si vous êtes appelé en entrevue, vous avez une probabilité élevée d’être recruté. L’entreprise
                        veut simplement s’assurer que votre messager (CV) n’a pas menti ou exagéré sur vous et que vous
                        correspondez exactement à la description de votre CV.</p>
                    <p>Au-delà de cela, l’entreprise voudrait voir si vous correspondez à son image, si vous pouvez
                        travailler et collaborer avec les autres collègues.</p>
                    <p>Tenant compte des objectifs de l’entrevue, il est important de porter attention aux choses
                        intangibles (votre image/apparence) et tangibles (préparation/réussite de l’entrevue).</p>
                    <p>Il est fondamental de soigner le look (cheveux, visage, tenue) et l’hygiène buccale et
                        corporelle.</p>
                    <p>Quant à l’entrevue, il est suggéré de faire :</p>
                    <ul>
                        <li>Une relecture de l’annonce, de l’offre d’emploi et sa candidature.</li>
                        <li>Une préparation des questions sur le poste à pourvoir, sur le manager de l’entreprise et sur
                            l’entreprise.</li>
                        <li>Une préparation des arguments et réponses.</li>
                        <li>Une préparation aux questions-pièges</li>
                    </ul>
                    <p>Si vous avez besoin d’une aide personnalisée dans la préparation de votre CV, de votre lettre de
                        motivation et votre entrevue d’embauche, vous pouvez <a href="#">cliquer ici et nous
                            contacter</a>.</p>

                    <h5>Gestion de carrière et Développement personnel</h5>
                    <p>Obtenir un emploi est une chose et le garder et atteindre ses objectifs dans la vie en est une
                        autre.</p>
                    <p>Même les gens qui ont un bon réseau reconnaissent que la plus grande difficulté est l’avancement
                        dans la carrière professionnelle. Ceci dépend, en grande partie, de notre comportement au
                        travail, de la qualité de notre travail et de notre développement personnel.</p>

                    <h5>a. Comportement au travail</h5>
                    <p>Dès notre premier jour au travail, il est important d’être perçu comme un professionnel positif,
                        optimiste, travailleur et honnête.</p>
                    <p>Vos collègues doivent voir en vous une valeur ajoutée, quelqu’un qui pourrait les appuyer dans
                        leur travail, en cas de besoin.</p>
                    <p>Ceci implique nécessairement de s’éloigner des clans, du bavardage et de la calomnie. Vous
                        devriez éviter, à tout prix, de critiquer vos collègues ou de prendre position sur un sujet que
                        vous ne maitrisez pas.</p>
                    <p>Il est fondamental d’être ponctuel et respectueux envers les autres.</p>
                    <p>Profitez de la courtoisie (éphémère) de bienvenue pour rencontrer vos collègues et leur poser, si
                        possible, des questions générales et spécifiques sur l’environnement de travail.</p>
                    <p>Soyez attentifs pour comprendre les dits et les non-dits. Evitez des questions indiscrètes ou
                        délicates comme la raison du départ de tel employé (que vous avez probablement remplacé).</p>

                    <h5>b. Qualité du travail</h5>
                    <p>Les premiers jours au travail devraient être consacrés à une meilleure compréhension de la
                        culture organisationnelle.</p>
                    <p>Après votre briefing de sécurité et votre tour au travail, il serait bien de rencontrer votre
                        superviseur et de discuter avec lui de votre description de taches. Ceci vous permettrait de
                        mieux cerner votre travail et de comprendre ce qui est attendu de vous.</p>
                    <p>Profitez de la période d’observation, de l’accalmie des premiers jours pour lire sur l’entreprise
                        et sa vision, pour vous familiariser avec les règlements internes et l’organigramme (qui
                        fait/décide quoi).</p>
                    <p>Il faudrait aussi lire les précédents rapports et comprendre la culture rédactionnelle et voir le
                        lien entre les recommandations et les décisions.</p>
                    <p>Une fois ce cap passé, il faudrait vous mettre sérieusement au travail afin de produire. Votre
                        premier travail ou votre premier rapport ou votre première intervention est clé et enverra un
                        signal sur votre capacité. La première impression est, en général, la bonne. La perception, en
                        général, l’emporte sur la réalité.</p>
